package project;

public class UsedCar {
    private double mileage;
    private double price;

    public UsedCar(double mileage, double price) {
        this.mileage = mileage;
        this.price = price;
    }

    public double getMileage() {
        return mileage;
    }

    public double getPrice() {
        return price;
    }
}
